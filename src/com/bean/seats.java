package com.bean;
/*
 * 座位信息类，包括id、位置号码、位置状态。
 */
public class seats {
private int id;
private int no;
private int state;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public int getState() {
	return state;
}
public void setState(int state) {
	this.state = state;
}

}
